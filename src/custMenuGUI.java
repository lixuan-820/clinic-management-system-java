    public custMenuGUI(Customer customerId) {
        if (customerId == null || customerId.getId().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Invalid customer session");
            System.exit(1);
        }
        this.customerId = customerId;
        initComponents();
        setLocationRelativeTo(null);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); 

        currentCustomer = Customer.loadCustomer(customerId);
        if (currentCustomer == null) {
            JOptionPane.showMessageDialog(this, "Customer not found: " + customerId,
                    "Error", JOptionPane.ERROR_MESSAGE);
            dispose();
            return;
        }
        setTitle("Customer Main Menu - " + currentCustomer.getFullName());
        jLabel1.setText("Welcome, " + currentCustomer.getFullName());
    }
    
