    public boolean validatePassword(String password, String confirmPassword){
        if (password.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in both password fields.");
            return false;
        }
        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(null, "Passwords do not match.");
            return false;
        }
        if (password.length() < 8) {
        JOptionPane.showMessageDialog(null, "Password must be at least 8 characters long.");
        return false;
        }
        if (!password.matches(".*[A-Za-z].*") || !password.matches(".*[0-9].*")) {
            JOptionPane.showMessageDialog(null, "Password must contain both letters and numbers.");
            return false;
        }
        return true;
    }
    
    public void togglePasswordVisibility(JPasswordField field){
        if (field.getEchoChar() != (char) 0) {
            field.setEchoChar((char) 0);
        }
        else {
            field.setEchoChar('*');
        }
    }
    
