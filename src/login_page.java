    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user_ID = jTextField1.getText().trim().toUpperCase();
        String password = new String(jPasswordField1.getPassword());

        if (User.checkLogin(user_ID, password)) {
            JOptionPane.showMessageDialog(this, "Login Successfully! Welcome " + user_ID);

            if (user_ID.startsWith("D")) {
                Doctor loggedInDoctor = FileHandler.loadDoctorByID(user_ID);
                if (loggedInDoctor != null) {
                    main_doctor mainPage = new main_doctor(loggedInDoctor);
                    mainPage.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Doctor details not found");
                }
            } else if(user_ID.startsWith("C")){
                Customer loggedInCustomer = FileHandler.loadCustomerByID(user_ID);
                if (loggedInCustomer != null) {
                    custMenuGUI mainPage = new custMenuGUI(loggedInCustomer);
                    mainPage.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Customer details not found");
                }
            } else if(user_ID.startsWith("M")){
                mainManager manager = new mainManager();
                manager.setVisible(true);
                this.dispose();
            } else if(user_ID.startsWith("S")){
                Staff loggedInStaff = FileHandler.loadStaffByID(user_ID);
                if (loggedInStaff != null) {
                    staffMainPage mainPage = new staffMainPage(loggedInStaff);
                    mainPage.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Staff details not found");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Login failed! Please check your ID or password!");
        }

        jTextField1.setText("");
        jPasswordField1.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

