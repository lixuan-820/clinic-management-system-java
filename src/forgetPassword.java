    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user_ID = jTextField1.getText().trim();
        String new_password = new String(jPasswordField1.getPassword());
        String confirm_password = new String(jPasswordField2.getPassword());

        if (new_password.equals(confirm_password)) {
            boolean ok = FileHandler.updatePassword(
                "D:\\APU\\Y2S1\\Object Oriented Development by Java\\NetBeansProjects\\Assignment_OODJ_G38\\Assignment_final\\data\\password.txt",
                user_ID,
                new_password
            );
            if (ok) {
                JOptionPane.showMessageDialog(this, "Password updated successfully!");
                login_page login = new login_page(doctorID);
                login.setVisible(true);
        // Optionally clear fields or close window
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Update failed! User ID not found.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "The passwords do not match. Please type again!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int choice = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to go back?",
            "Confirm",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE
    );

    if (choice == JOptionPane.YES_OPTION) {
        this.dispose(); // close Forgot Password window

        login_page login = new login_page(doctorID);
        login.setVisible(true);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

